"use client"

import * as React from "react"
import { type DialogProps } from "@radix-ui/react-dialog"
import { Command as CommandPrimitive } from "cmdk"
import { Search } from "lucide-react"

import { cn } from "@/lib/utils"
import { Dialog, DialogContent } from "@/components/ui/dialog"

const Command = React.forwardRef<
  React.ElementRef<typeof CommandPrimitive>,
  React.ComponentPropsWithoutRef<typeof CommandPrimitive>
>(({ className, ...props }, ref) => (
  <CommandPrimitive
    ref={ref}
    className={cn(
      "flex h-full w-full flex-col overflow-hidden rounded-md bg-popover text-popover-foreground",
      className
    )}
    {...props}
  />
))
Command.displayName = CommandPrimitive.displayName

type CommandDialogProps = DialogProps & {
  /** Forwarded to the inner cmdk `Command`. Pass `shouldFilter={false}` to
   *  disable cmdk's built-in fuzzy filter when the caller is doing its own
   *  filtering (e.g. the global ⌘K palette). */
  commandProps?: React.ComponentPropsWithoutRef<typeof CommandPrimitive>
  /** Forwarded to the underlying `DialogContent`. Defaults to `true` so existing
   *  callers keep the top-right close X; pass `false` when the command surface
   *  owns its own input row and the inherited X would overlap it (the ⌘K
   *  palette). Esc-to-close is unaffected. */
  showCloseButton?: boolean
}

const CommandDialog = ({
  children,
  commandProps,
  showCloseButton,
  ...props
}: CommandDialogProps) => {
  return (
    <Dialog {...props}>
      <DialogContent
        showCloseButton={showCloseButton}
        className="overflow-hidden p-0 shadow-lg"
      >
        <Command
          {...commandProps}
          className="[&_[cmdk-group-heading]]:px-2 [&_[cmdk-group-heading]]:font-medium [&_[cmdk-group-heading]]:text-muted-foreground [&_[cmdk-group]:not([hidden])_~[cmdk-group]]:pt-0 [&_[cmdk-group]]:px-2 [&_[cmdk-input-wrapper]_svg]:h-5 [&_[cmdk-input-wrapper]_svg]:w-5 [&_[cmdk-item]]:px-2 [&_[cmdk-item]]:py-3 [&_[cmdk-item]_svg:not([class*='size-'])]:size-5"
        >
          {children}
        </Command>
      </DialogContent>
    </Dialog>
  )
}

